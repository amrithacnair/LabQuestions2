import java.util.*;
class CharactersFromAtoZ
{
 public static void main(String[] args)
{
 char ch;
 System.out.println("The Alphabets in Capital Letters");
 for(ch='A';ch<='Z';ch++)
 {
  System.out.println(ch);
 }
 System.out.println("The Alphabets in Small Letters");
 for(ch='a';ch<='z';ch++)
 {
  System.out.println(ch);
 }
}
}